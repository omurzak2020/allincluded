package com.example.horizontalrecycler;

public class HackSmileModel {
    private int image;
    private String title;

    public HackSmileModel(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }


    public String getTitle() {
        return title;
    }


}
